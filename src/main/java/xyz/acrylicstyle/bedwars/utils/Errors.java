package xyz.acrylicstyle.bedwars.utils;

public enum Errors {
    /**
     * An error has occurred while scheduling task.
     */
    SCHEDULER_ERROR,
    /**
     * Couldn't warp player.
     */
    COULD_NOT_WARP,
    /**
     * An unknown error has occurred and we don't know what caused the error or an error has occurred that not listed at Errors enum.
     */
    UNKNOWN_ERROR
}
